    // ImprovedList 자체 락을 사용하며 List와는 다른 수준의 락을 활용
    private final List<T> list;

    public Ex_4_16_ImprovedList(List<T> list) {
        this.list = list;
    }

    public synchronized boolean putIfAbsent(T x) {
        boolean contains = list.contains(x);
        if (!contains) {
            list.add(x);
        }
        return !contains;
    }

